import numpy as np

# Create NumPy Array

arr = np.array([10, 20, 30, 40, 50])

print("Original Array:")
print(arr)

# Arithmetic Operations
print("\nAddition:")
print(arr + 5)

print("\nSubtraction:")
print(arr - 5)

print("\nMultiplication:")
print(arr * 2)

print("\nDivision:")
print(arr / 2)



####    OUTPUT     ####

# Original Array:
# [10 20 30 40 50]

# Addition:
# [15 25 35 45 55]

# Subtraction:
# [ 5 15 25 35 45]

# Multiplication:
# [ 20  40  60  80 100]

# Division:
# [ 5. 10. 15. 20. 25.]